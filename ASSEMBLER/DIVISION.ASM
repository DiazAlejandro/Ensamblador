DATOS SEGMENT ; DEFINICIÓN DEL SEGMENTO DE DATOS
    DIVIDENDO DW 5     ; DIVIDENDO DE 16 BITS
    DIVISOR   DB 2     ; DIVISOR DE 8 BITS
    COCIENTE DB ?      ; COCIENTE DE 8 BITS
    RESIDUO  DB ?      ; RESIDUO DE 8 BITS
DATOS ENDS    ; FIN DEL SEGMENTO DE DATOS

PILA SEGMENT STACK "STACK" ; DEFINICIÓN DEL SEGMENTO DE PILA
PILA ENDS                  ; FIN DEL SEGMENTO DE PILA

CODE SEGMENT "CODE"       ; DEFINICÓN DEL SEGMENTO DE CÓDIGO
    ASSUME CS:CODE,DS:DATOS,SS:PILA ; LLAMA LOS SEGMENTOS DEL CÓDIGO
    START PROC FAR ; INICIO DEL PROCEDIMIENTO PRINCIPAL 
        MOV AX,DATOS      ; MUEVE EL SEGMENTO DE DATOS A AX
        MOV DS,AX         ; MUEVE A DS:=AX

        MOV AX,DIVIDENDO  ; MUEVE EL 5 AL REGISTRO AX (DIVIDENDO)
        MOV BL,DIVISOR    ; MUEVE EL 2 AL REGISTRRO BL (DIVISOR)
        DIV BL            ; DIVISION DE BL
        ADD AL,48         ; SUMA LOS 48 POR EL ASCII
        
        MOV COCIENTE,AL   ; ASIGNA EL VALOR DE AL AL COCIENTE
        MOV RESIDUO,AH    ; ASIGNA EL VALOR DE AH AL RESIDUO 

        MOV DL,COCIENTE   ; MUEVE EL VALOR DEL COCIENTE AL REGISTRO DL
        MOV AH,02H        ; INSTRUCCIÓN PARA COLOCAR EL CURSOR
        INT 21H           ; PETICIÓN DE FUNCIÓN AL DOS

    START ENDP     ; FIN DEL PROCEDIMIENTO PRINCIPAL
CODE ENDS          ; FIN DEL SEGMENTO DE CÓDIGO
END START          ; FIN DEL PROGRAMA