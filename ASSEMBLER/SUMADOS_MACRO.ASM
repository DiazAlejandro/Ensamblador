PRINT MACRO CADENA
    MOV AH,09H
    LEA DX,CADENA
    INT 21H
ENDM

IMPRIME_CARACTER MACRO CARACTER 
    MOV AH,02H
    MOV DL,CARACTER
    INT 21H
ENDM

.MODEL SMALL 
.STACK
.DATA
    PEDIR DB ' INGRESA UN NUMERO: $'
    DATO1 DB ?,'$'

.CODE
    MAIN PROC FAR 
        MOV AX,@DATA
        MOV DS,AX

        PRINT PEDIR
        CALL LEER_NO
        MOV DATO1, AL

        PRINT PEDIR
        CALL LEER_NO
        ADD DATO1,AL
        SUB DATO1,30h
        PRINT DATO1
.EXIT
MAIN ENDP

    LEER_NO PROC
        MOV AH,01H
        INT 21H
        RET
    ENDP
END MAIN 