TITLE 'WHILE'
; EJEMPLO DE LA INSTRUCCIÓN WHILE
; INSTRUCCIÓN EN LENGUAJE DE ALTO NIVEL
; WHILE (A>B) DO
;     B:=B+1
; DONDE A = 5 
;       B = 0

DATOS SEGMENT ; DEFINICIÓN DEL SEGMENTO DE DATOS
    A DB 5    ; VALOR LÍMITE 
    B DB 0    ; VALOR QUE FUNCIONARÁ COMO INCREMENTO
    NOMBRE DB 'ALEJANDRO$'
DATOS ENDS    ; FIN DEL SEGMENTO DE DATOS

PILA SEGMENT STACK "STACK" ; DEFINICIÓN DEL SEGMENTO DE PILA
PILA ENDS                  ; FIN DEL SEGMENTO DE PILA

CODE SEGMENT "CODE" ; DEFINICIÓN DEL SEGMENTO DE CÓDIDGO
    ASSUME CS:CODE,DS:DATOS,SS:PILA ; LLAMA LOS SEGMENTOS DEL CÓDIGO
    START PROC      ; INICIO DEL PROCEDIMIENTO PRINCIPAL
        MOV AX,DATOS        ; MUEVE EL SEGMENTO DE DATOS A AX
        MOV DS,AX           ; MUEVE A DATA SEGMENT,AX

        MOV BL,A            ; BL CONTENDRÁ EL VALOR LÍMITE
        
        MIENTRAS: CMP BL,B  ; COMPARA A CONTRA B?
            JB  FIN         ; SALTA SI BL<B
            ;INSTRUCCIONES QUE SE EJECUTAN MIENTRAS A>B
            INC B           ; INCREMENTA UNO A 'B'
            ;IMPRIMIR
            MOV AH,9H       ; INTERRUPCIÓN       
            LEA DX,NOMBRE   ; CARGA LA DIRECCIÓN DE MEMORIA
            INT 21H         ; PETICIÓN DE FUNCIÓN AL DOS  

            ;SALTO DE LINEA
            MOV DL, 10      ; NUEVA LINEA
            MOV AH, 02H     ; INSTRUCCIÓN PARA COLOCAR EL CURSOR
            INT 21H         ; PETICIÓN DE FUNCIÓN AL DOS

            JMP MIENTRAS    ; SALTAR AL CMP (WHILE)
        FIN: 


    ENDP START      ; FIN DEL PROCEDIMIENTO PRINCIPAL
CODE ENDS           ; FIN DEL SEGMENTO DE CODIGO 
END START           ; FIN DEL PROGRAMA